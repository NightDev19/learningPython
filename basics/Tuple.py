# Tuples
'''
==============
TUPLES
==============
A READ-ONLY collection of varaibles may be used to sort a certain data

Tuples
1. Can be Read
2. Can be Combined
3. Can be Deleted Completely
4. Can't be Assigned
5. Can't be Deleted one by one 

Syntax:
identifier = (value,value1,value2)
Example:
courses = ("BSIT","BSCS","BSIS")
'''

#How to use a Tuple
courses = ("BSIT","BSCS","BSIS")
print(courses)
# Reading a tuple using a for loop
for data in range(len(courses)):
    print(courses[data])
    
